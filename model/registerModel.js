import mongoose from "mongoose"

const registerSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ["user", "admin"],
        default: "user"
    }



})

const registerModel = mongoose.model("register", registerSchema)

export default registerModel
